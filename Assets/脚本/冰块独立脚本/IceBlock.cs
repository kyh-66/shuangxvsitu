using UnityEngine;

public class IceBlock : MonoBehaviour
{
    [Header("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ")]
    public float existTime = 2.0f; // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฺต๏ฟฝสฑ๏ฟฝไฃจ๏ฟฝ๋ฃฉ
    public float fallSpeed = 3.0f; // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฃ๏ฟฝิฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝิฝ๏ฟฝ์ฃฉ

    private Rigidbody2D rb;
    private Collider2D col;

    void Start()
    {
        rb = GetComponent<Rigidbody2D>();
        col = GetComponent<Collider2D>();

       
        Invoke("StartFalling", existTime);
    }

    void StartFalling()
    {
        // 1. ๏ฟฝะป๏ฟฝฮช๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝำฐ๏ฟฝ์ฃฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝศฅ
        rb.bodyType = RigidbodyType2D.Dynamic;
        rb.gravityScale = fallSpeed;

        
        if (col != null)
        {
            col.isTrigger = true;
        }

        
        Destroy(gameObject, 2.0f);
    }
}